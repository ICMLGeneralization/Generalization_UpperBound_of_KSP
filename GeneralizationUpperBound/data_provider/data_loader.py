class Dataset_ETT_hour(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h'):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))

        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            data_stamp = df_stamp.drop(['date'], axis=1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0)

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)


class Dataset_ETT_hour_mixture(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h',
                 a=1.0, seed=time.time()%7, shuffle=True, mix_len=None):
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]

        assert flag in ['train', 'test', 'val']
        assert 0.0 <= a <= 1.0

        self.flag = flag

        self.features = features
        self.target = target
        self.scale = scale
        self.timeenc = timeenc
        self.freq = freq

        self.a = float(a)
        self.seed = int(seed)
        self.shuffle = bool(shuffle)
        self.mix_len = mix_len

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path, self.data_path))

        border1s = [0, 12 * 30 * 24 - self.seq_len, 12 * 30 * 24 + 4 * 30 * 24 - self.seq_len]
        border2s = [12 * 30 * 24, 12 * 30 * 24 + 4 * 30 * 24, 12 * 30 * 24 + 8 * 30 * 24]
        type_map = {'train': 0, 'val': 1, 'test': 2}
        set_type = type_map[self.flag]

        border1_train = border1s[0]
        border2_train = border2s[0]
        border1_test = border1s[2]
        border2_test = border2s[2]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1_train:border2_train]
            self.scaler.fit(train_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        def _build_stamp(df_stamp):
            df_stamp = df_stamp.copy()
            df_stamp['date'] = pd.to_datetime(df_stamp.date)
            if self.timeenc == 0:
                df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
                df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
                df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
                df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
                data_stamp = df_stamp.drop(['date'], axis=1).values
            elif self.timeenc == 1:
                data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
                data_stamp = data_stamp.transpose(1, 0)
            return data_stamp

        if self.flag == 'test':
            self.data_x_train = data[border1_train:border2_train]
            self.data_y_train = data[border1_train:border2_train]
            self.data_stamp_train = _build_stamp(df_raw[['date']][border1_train:border2_train])

            self.data_x_test = data[border1_test:border2_test]
            self.data_y_test = data[border1_test:border2_test]
            self.data_stamp_test = _build_stamp(df_raw[['date']][border1_test:border2_test])

            len_train = len(self.data_x_train) - self.seq_len - self.pred_len + 1
            len_test = len(self.data_x_test) - self.seq_len - self.pred_len + 1
            assert len_train > 0
            assert len_test > 0
            if self.mix_len is None:
                if self.a == 0.0:
                    train_count = len_train
                    test_count = 0
                elif self.a == 1.0:
                    train_count = 0
                    test_count = len_test
                else:
                    train_count = int((1.0 - self.a) * len_train)
                    test_count = int(self.a * len_test)
                total_len = train_count + test_count
            else:
                total_len = int(self.mix_len)
                test_count = int(total_len * self.a)
                train_count = total_len - test_count
            assert total_len > 0

            np.random.seed(int(self.seed * time.time() % 3))
            rng = np.random.RandomState(int(self.seed * time.time() % 3))
            train_replace = train_count > len_train
            test_replace = test_count > len_test
            train_indices = rng.choice(len_train, size=train_count, replace=train_replace) if train_count > 0 else np.array([], dtype=np.int64)
            test_indices = rng.choice(len_test, size=test_count, replace=test_replace) if test_count > 0 else np.array([], dtype=np.int64)

            mix_index = [(0, int(i)) for i in train_indices] + [(1, int(i)) for i in test_indices]
            if self.shuffle:
                rng.shuffle(mix_index)
            self.mix_index = mix_index
        else:
            border1 = border1s[set_type]
            border2 = border2s[set_type]
            self.data_x = data[border1:border2]
            self.data_y = data[border1:border2]
            self.data_stamp = _build_stamp(df_raw[['date']][border1:border2])
            self.mix_index = None

    def __getitem__(self, index):
        if self.mix_index is None:
            s_begin = index
            s_end = s_begin + self.seq_len
            r_begin = s_end - self.label_len
            r_end = r_begin + self.label_len + self.pred_len

            seq_x = self.data_x[s_begin:s_end]
            seq_y = self.data_y[r_begin:r_end]
            seq_x_mark = self.data_stamp[s_begin:s_end]
            seq_y_mark = self.data_stamp[r_begin:r_end]

            return seq_x, seq_y, seq_x_mark, seq_y_mark
        else:
            set_id, local_index = self.mix_index[index]
            if set_id == 0:
                data_x = self.data_x_train
                data_y = self.data_y_train
                data_stamp = self.data_stamp_train
            else:
                data_x = self.data_x_test
                data_y = self.data_y_test
                data_stamp = self.data_stamp_test

            s_begin = local_index
            s_end = s_begin + self.seq_len
            r_begin = s_end - self.label_len
            r_end = r_begin + self.label_len + self.pred_len

            seq_x = data_x[s_begin:s_end]
            seq_y = data_y[r_begin:r_end]
            seq_x_mark = data_stamp[s_begin:s_end]
            seq_y_mark = data_stamp[r_begin:r_end]

            return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        if self.mix_index is None:
            return len(self.data_x) - self.seq_len - self.pred_len + 1
        else:
            return len(self.mix_index)

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)

